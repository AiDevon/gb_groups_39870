# По введенным пользователем координатам двух точек вывести уравнение прямой вида y = kx + b,
# проходящей через эти точки

x1 = int(input("Введите координаты точки A x1 >>> "))
y1 = int(input("Введите координаты точки A y1 >>> "))
x2 = int(input("Введите координаты точки B x2 >>> "))
y2 = int(input("Введите координаты точки B y1 >>> "))

k = (y1 - y2) / (x1 - x2)
b = y2 - k * x2

print(f"k = {k}")
print(f"b = {b}")